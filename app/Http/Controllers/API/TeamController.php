     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $teamObj = DB::table('teams')->where('id', $id)->delete();

        if ($teamObj) {
            return redirect()->back()->with('success','Team deleted successfully!');
