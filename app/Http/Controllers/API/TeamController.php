    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $validate = $request->validate([
                'team' => 'required',
            ],
        );

        $teamObj = new Team();
        $teamObj->team = $request->team;
        $teamObj->save();

        if ($validate && $teamObj) {
            return redirect()->route('api.teams')->with('success','New team added successfully!');
        } else {
            return redirect()->back()->with('error','It was not possible to add a new team!');
        }
    }

